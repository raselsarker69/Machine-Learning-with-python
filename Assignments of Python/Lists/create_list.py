# There are several kind of list. For example blank lists, Num lists, and items lists.

print("Creating a Blank List:")
blank_list = []
print(f"Blank List: {blank_list}\n")

print("Creating a Number List:")
num_list = [10, 20, 30, 40, 50]
print("Number List:", num_list, "\n")

print("Creating an Item List:")
item_list = ['Mango', 'Banana', 'Jackfruit']
print("Item List:", item_list)